// -- Response types

#[derive(Debug, Serialize)]
pub struct ExportAgentResponse {
	pub id: i64,
	pub name: String,
	pub export_format: String,
}

#[derive(Debug, Serialize)]
pub struct ArchiveAgentResponse {
	pub id: i64,
	pub archived: bool,
}

#[derive(Debug, Serialize)]
pub struct AgentReportResponse {
	pub total: i64,
	pub report_type: String,
}

/// Demo 1: #[register_permission] - Only registers the permission, no runtime check
/// The permission will be synced to database at startup.
/// Manual check still needed.
#[lib_macros::register_permission(
	key = "agent:export",
	group = "Agent Management",
	display = "Export Agent",
	description = "Export agent data in various formats"
)]
pub async fn export_agent(
	ctx: CtxW,
	State(mm): State<ModelManager>,
	Path(PathId { id }): Path<PathId>,
) -> Result<RestResponse<ExportAgentResponse>> {
	let ctx = ctx.0;
	// Manual permission check (register_permission only registers, not checks)
	ctx.require_permission("agent:export")?;

	let agent = AgentBmc::get(&ctx, &mm, id).await?;

	Ok(ExportAgentResponse {
		id: agent.id,
		name: agent.name,
		export_format: "json".to_string(),
	}.into())
}

/// Demo 2: #[rest_permission] - Both registers AND checks (recommended for REST)
/// Uses ctx.0 automatically for CtxW wrapper!
#[lib_macros::rest_permission(
	key = "agent:archive",
	group = "Agent Management",
	display = "Archive Agent",
	description = "Archive an agent to mark it as inactive"
)]
pub async fn archive_agent(
	ctx: CtxW,
	State(mm): State<ModelManager>,
	Path(PathId { id }): Path<PathId>,
) -> Result<RestResponse<ArchiveAgentResponse>> {
	// Permission check is automatically injected by #[rest_permission]!
	let _agent = AgentBmc::get(&ctx.0, &mm, id).await?;

	Ok(ArchiveAgentResponse {
		id,
		archived: true,
	}.into())
}

/// Demo 3: #[rest_require_permissions] - Check multiple permissions (ALL must pass)
/// Auto-injects ctx.0.require_all_permissions()
#[lib_macros::rest_require_permissions("agent:read", "agent_custom:read")]
pub async fn agent_report(
	ctx: CtxW,
	State(mm): State<ModelManager>,
) -> Result<RestResponse<AgentReportResponse>> {
	// Permission checks are automatically injected!
	let total = AgentBmc::count(&ctx.0, &mm, None).await?;

	Ok(AgentReportResponse {
		total,
		report_type: "summary".to_string(),
	}.into())
}

/// Demo 4: #[rest_require_any_permission] - Check multiple permissions (ANY one passes)
/// Auto-injects ctx.0.require_any_permission()
#[lib_macros::rest_require_any_permission("agent:read", "agent_custom:read")]
pub async fn view_agent(
	ctx: CtxW,
	State(mm): State<ModelManager>,
	Path(PathId { id }): Path<PathId>,
) -> Result<RestResponse<Agent>> {
	// Permission check is automatically injected!
	let agent = AgentBmc::get(&ctx.0, &mm, id).await?;
	Ok(agent.into())
}

