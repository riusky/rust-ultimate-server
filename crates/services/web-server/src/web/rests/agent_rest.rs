use serde::{Deserialize, Serialize};

generate_common_rest_fns!(
	Bmc: AgentBmc,
	Entity: Agent,
	ForCreate: AgentForCreate,
	ForUpdate: AgentForUpdate,
	Filter: AgentFilter,
	Suffix: agent,
	ResourceDisplay: "Agent",
	ResourceGroup: "Agent Management",
	ResourceDescription: "agent entity for AI assistants"
);

// region:    --- Custom REST Endpoints

// -- Request/Response types for custom endpoints

#[derive(Debug, Deserialize)]
pub struct CloneAgentPayload {
	pub new_name: String,
}

#[derive(Debug, Serialize)]
pub struct CloneAgentResponse {
	pub original_id: i64,
	pub cloned_id: i64,
	pub cloned_agent: Agent,
}

#[derive(Debug, Serialize)]
pub struct AgentStatsResponse {
	pub total_count: i64,
	pub active_count: i64,
}

// -- Permission registration for custom endpoints

lib_core::register_crud_permissions!("agent_custom", "Agent Custom", "Agent Management", "custom agent operations");

// -- Custom endpoint handlers

/// POST /agents/:id/clone
/// Clone an existing agent with a new name
#[lib_macros::rest_permission(
	key = "agent_custom:create",
	group = "Agent Management",
	display = "Clone Agent",
	description = "Create a copy of an existing agent with a new name"
)]
pub async fn clone_agent(
	ctx: CtxW,
	State(mm): State<ModelManager>,
	Path(PathId { id }): Path<PathId>,
	Json(payload): Json<CloneAgentPayload>,
) -> Result<RestCreated<CloneAgentResponse>> {
	// Permission check is automatically injected!

	// Get original agent
	let original = AgentBmc::get(&ctx.0, &mm, id).await?;

	// Create cloned agent with new name
	let agent_c = AgentForCreate {
		name: payload.new_name,
	};
	let cloned_id = AgentBmc::create(&ctx.0, &mm, agent_c).await?;
	let cloned_agent = AgentBmc::get(&ctx.0, &mm, cloned_id).await?;

	Ok(CloneAgentResponse {
		original_id: original.id,
		cloned_id,
		cloned_agent,
	}.into())
}

/// GET /agents/stats
/// Get agent statistics
#[lib_macros::rest_permission(
	key = "agent_custom:read",
	group = "Agent Management",
	display = "Get Agent Stats",
	description = "View agent statistics including total and active counts"
)]
pub async fn get_agent_stats(
	ctx: CtxW,
	State(mm): State<ModelManager>,
) -> Result<RestResponse<AgentStatsResponse>> {
	// Permission check is automatically injected!

	// Get total count
	let total_count = AgentBmc::count(&ctx.0, &mm, None).await?;

	// For demo, active_count = total_count (no status field yet)
	let active_count = total_count;

	Ok(AgentStatsResponse {
		total_count,
		active_count,
	}.into())
}

/// Build custom agent routes (not generated by macro)
pub fn custom_agent_routes() -> axum::Router<ModelManager> {
	use lib_core::generate_custom_rest_routes;

	// generate_custom_rest_routes! validates AND builds router in one step
	// No need to maintain two separate lists!
	generate_custom_rest_routes!(
		get "/stats" => get_agent_stats,
		post "/{id}/clone" => clone_agent,
		get "/{id}/export" => export_agent,
		post "/{id}/archive" => archive_agent,
		get "/report" => agent_report,
		get "/{id}/view" => view_agent,
	)
}

